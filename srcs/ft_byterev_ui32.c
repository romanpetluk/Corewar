/*
** Change byte order (little endian to big endian or vice versa)
**
** Example:
** 		0000 0001 0110 0111 -> 0111 0110 0001 0000
*/

void	ft_byterev_ui32(unsigned int *i)
{
	*i = (*i >> 24 & 0xff) |
		(*i << 8 & 0xff0000) |
		(*i >> 8 & 0xff00) |
		(*i << 24 & 0xff000000);
}
