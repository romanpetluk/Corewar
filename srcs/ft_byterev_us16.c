/*
** Change byte order (little endian to big endian or vice versa)
**
** Example:
** 		0110 0111 -> 0111 0110
*/

void	ft_byterev_us16(unsigned short *i)
{
	*i = (*i >> 8 & 0xff) |
		(*i << 8 & 0xff00);
}
